	const [shellGeometry, shellEdgesGeometry] = useMemo(() => {
		const geometry = new THREE.BoxGeometry(widthFt, depthFt, heightFt)
		return [geometry, new THREE.EdgesGeometry(geometry)] as const
	}, [depthFt, heightFt, widthFt])

	useEffect(() => {
		return () => {
			shellGeometry.dispose()
			shellEdgesGeometry.dispose()
		}
	}, [shellEdgesGeometry, shellGeometry])

	const accentColor = snapActive ? '#4ade80' : guide ? '#8b5cf6' : '#60a5fa'
	const shellColor = snapActive ? '#d9f99d' : guide ? '#ddd6fe' : '#bfdbfe'
