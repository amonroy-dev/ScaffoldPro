function getBlockWorldRect(block: Pick<ScaffoldBlockInstance, 'id' | 'center' | 'widthFt' | 'depthFt' | 'rotationSteps'>): BlockWorldRect {
	const halfWidthFt = block.widthFt / 2
	const halfDepthFt = block.depthFt / 2
	const corners = [
		rotateOffset90({ x: -halfWidthFt, y: -halfDepthFt }, block.rotationSteps ?? 0),
		rotateOffset90({ x: halfWidthFt, y: -halfDepthFt }, block.rotationSteps ?? 0),
		rotateOffset90({ x: halfWidthFt, y: halfDepthFt }, block.rotationSteps ?? 0),
		rotateOffset90({ x: -halfWidthFt, y: halfDepthFt }, block.rotationSteps ?? 0),
	]
	const xs = corners.map((corner) => block.center.x + corner.x)
	const ys = corners.map((corner) => block.center.y + corner.y)
	return {
		id: block.id,
		xMin: Math.min(...xs),
		xMax: Math.max(...xs),
		yMin: Math.min(...ys),
		yMax: Math.max(...ys),
	}
}

