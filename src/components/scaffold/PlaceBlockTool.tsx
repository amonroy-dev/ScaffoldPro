function BlockFootprint(props: {
	id: string
	center: { x: number; y: number }
	rotationSteps: number
	widthFt: number
	depthFt: number
	/** When false, the footprint is display-only (no selection handlers attached). */
	selectable: boolean
	selected: boolean
	onSelect: (id: string, additive: boolean, event: ThreeEvent<PointerEvent>) => void
	onHoverChange?: (id: string | null) => void
}) {
	const { id, center, rotationSteps, widthFt, depthFt, selectable, selected, onSelect, onHoverChange } = props
	const handleBlockedClick = useCallback((e: ThreeEvent<PointerEvent>) => {
		// In Place mode we want to *prevent* accidental placement on top of an existing block.
		// So we swallow the event but do not select.
		e.stopPropagation()
		e.nativeEvent.stopImmediatePropagation?.()
	}, [])
		const perimeterEdgesGeometry = useMemo(() => {
			// Use EdgesGeometry + lineSegments to avoid TSX intrinsic-element collisions with SVG <line>.
			const plane = new THREE.PlaneGeometry(widthFt, depthFt)
			return new THREE.EdgesGeometry(plane)
		}, [widthFt, depthFt])

	const handleSelect = useCallback((e: ThreeEvent<PointerEvent>) => {
		if (e.button !== 0) return
		if (isCameraNavigationModifierGesture(e.nativeEvent)) return
		e.stopPropagation()
			// Prevent camera-controls from starting a pan/rotate on this same pointer down.
			e.nativeEvent.stopImmediatePropagation?.()
		onSelect(id, !!(e.nativeEvent.ctrlKey || e.nativeEvent.metaKey), e)
	}, [id, onSelect])

	return (
		<group
			position={[center.x, center.y, 0.02]}
			rotation={[0, 0, rotationSteps * (Math.PI / 2)]}
		>
			<mesh
				onPointerDown={selectable ? handleSelect : handleBlockedClick}
				onPointerEnter={selectable ? () => onHoverChange?.(id) : undefined}
				onPointerLeave={selectable ? () => onHoverChange?.(null) : undefined}
			>
				<planeGeometry args={[widthFt, depthFt]} />
				<meshBasicMaterial
					transparent
					opacity={selected ? 0.08 : 0.03}
					color={selected ? '#4ade80' : '#8ab4ff'}
						side={THREE.DoubleSide}
					depthWrite={false}
				/>
			</mesh>

				<lineSegments
					onPointerDown={selectable ? handleSelect : handleBlockedClick}
					onPointerEnter={selectable ? () => onHoverChange?.(id) : undefined}
					onPointerLeave={selectable ? () => onHoverChange?.(null) : undefined}
				>
					<primitive object={perimeterEdgesGeometry} attach="geometry" />
					<lineBasicMaterial transparent opacity={selected ? 0.9 : 0.55} color={selected ? '#4ade80' : '#8ab4ff'} />
				</lineSegments>
		</group>
	)
}

